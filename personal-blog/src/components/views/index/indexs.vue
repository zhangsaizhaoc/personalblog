                    <h6>善自嘲而不嘲人</h6>
                    <p>为人处事中，言语上短兵相接，以退为进、欲擒故纵往往能出奇制胜——这种迂回进攻的方法，就是自嘲。</p>
                    <p>一句随意的言语、一个简单的动作，往往就能在顷刻间轻轻松松地给自己解困。自嘲的人，别人不敢嘲笑，因为他先发制人地为自己挽回了面子，不卑不亢地让凝滞的空气重新流动起来。
    自嘲的人必定心中有爱，不但善待自己，也善待他人，不会用过分的自卑委屈自己，也不会用尖酸刻薄为难他人。自嘲，就是这样一种以退为进的智慧。
