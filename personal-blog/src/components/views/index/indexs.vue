    自嘲的人，是智者中的智者，高手中的高手。自嘲这项艺术只有充分自信的人才敢使用，因为它就是叫你自己骂自己，拿自己的缺陷和过失“开涮”；对于自己的丑处非但不遮遮掩掩，反而夸大其词，自圆其说地博取众人一笑，也展示自己的风度和智慧；甚至，还能刺一刺那些含沙射影、无理取闹的小人。
    所以说，自嘲的人，别人不敢嘲笑。自嘲，最安全。</p>
                      <h6>知世故而不世故</h6>
                      <p><b>言  菻</b></p>
                      <p>“孩子们都在狂奔，也不知道自己是在往哪儿跑，我得从什么地方出来，把他们捉住。我整天就干这样的事。我只想当个麦田里的守望者。”做一个麦田里的守望者，这是书中主人公霍尔顿的最大梦想，一个十六岁男孩最想做的事。
　　《麦田里的守望者》是美国作家杰罗姆·大卫·塞林格唯一的一部长篇小说。书中叙述了叛逆少年霍尔顿从被学校辞退到回家的三天内所发生的事情。霍尔顿是个非常叛逆的十六岁少年，他对身边的一切人和事都腻烦极了。他无视课业，也因此屡次被学校退学。这一次，他又因为考试多门不及格而再次被学校辞退。本该是星期三才回家，却因为实在忍受不了室友们而提前离开。他回到了纽约，却并不回家，而是在街头徘徊。他还未成年，却在酒吧喝酒。他招妓，最后只是和对方闲聊了会儿天。他厌烦一切人和事，却无比怀念早逝的弟弟艾里。他嘴里总说着脏话，内心却比谁都善良。他挂念着妹妹菲比，偷偷摸摸回家，表现的英勇无比，事实上却对被父母发现他已经回家而怕得要命。最后他想离开，去遥远的西部农场生活，但最终因为菲比而未能成行。故事的最后，霍尔顿回到了家，后来似乎又继续了学业，但作者并没有详细交代，故事在此戛然而止。
　　不得不说，作者对这本书的构思和情节铺设都是了不起的！他仅仅是描述了霍尔顿短短三天里的故事，却仿佛让我看到了十六岁少年的整个青春：厌世——叛逆——逃离——妥协。
　　十六岁，是一个人真正从稚嫩的孩童时代过渡到现实的大人世界的年纪。这个过程是十分痛苦的。那些血淋淋的现实不断冲击着我们对世界原本单纯天真的认知，我们的世界被摧毁，然后被可怕的现实一点点重建，重建成一个陌生的残酷的世界。也因此，我们就会像狼护犊子一样狠狠地拒绝这种重建，拒绝长大，拒绝现实的思想观念的入侵。我们排斥、叛逆，就像书中的霍尔顿一样，对一切都感到极其腻烦。而当一个人对周遭一起都感到无比腻烦的时候，就是一个人堕落的开始。于是，就有了现实生活中沉迷网络的少年、离家出走的少年以及各种离经叛道的少年。纯真与现实是否真的无法共存？有一句话说得好，“知世故而不世故，才是最善良的成熟”。我们可以明白现实的丑恶，但且谨记保持本身的良善。成长是一个人一生的必修科目，无论如何血腥，最后终会接受。霍尔顿因为排斥现实的虚伪而荒废学业，因为排斥现实的虚伪而想要远走他乡，但最终都没有成功。他没有去西部农场，也没有成为一个麦田里的守望者，他还是留在了纽约，病好后还得继续学业。就像是运行的火车，即使中途偏离了轨道，最后也要再偏回来继续既定旅程。这大概就是一种对现实生活的妥协。然而从另一方面来看，这也是一种成长。当我们必须去完成一件事的时候，我们不能因为讨厌、腻烦它而不去做。我们能做的是调整自己，调整心态。学会知世故而不世故，怀有一颗成为麦田里的守望者的心，做一个善良的成熟人。</p>
